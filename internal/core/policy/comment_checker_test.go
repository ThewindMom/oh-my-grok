func TestCheckLinePraise(t *testing.T) {
	r := CheckLine(1, "// Great function!", PolicyDeny)
	if r == nil {
		t.Fatal("expected result for praise comment")
	}
	if r.Category != "praise" {
		t.Errorf("category = %s, want praise", r.Category)
	}
	if r.Action != PolicyDeny {
		t.Errorf("action = %s, want deny", r.Action)
	}
}

