func TestCheckContent(t *testing.T) {
	content := `package main

// Great implementation!
func add(a, b int) int {
    // This function returns the sum
    return a + b
}
`
	results := CheckContent(content, PolicyDeny)
	if len(results) < 2 {
		t.Errorf("expected at least 2 results, got %d", len(results))
	}
}

func TestCheckContentAllowPolicy(t *testing.T) {
	content := "// Great function!\nfunc main() {}\n"
	results := CheckContent(content, PolicyAllow)
	if len(results) != 0 {
		t.Errorf("allow policy should return no results, got %d", len(results))
	}
}

func TestShouldDeny(t *testing.T) {
	results := []Result{
		{Line: 1, Action: PolicyDeny},
	}
	if !ShouldDeny(results) {
		t.Error("should deny when any result has deny action")
	}

	results = []Result{
		{Line: 1, Action: PolicyWarn},
	}
	if ShouldDeny(results) {
		t.Error("should not deny when no result has deny action")
	}
}

func TestFormatResults(t *testing.T) {
	results := []Result{
		{Line: 5, Comment: "// Great!", Category: "praise", Reason: "test"},
	}
	s := FormatResults(results)
	if !strings.Contains(s, "line 5") {
		t.Error("format should contain line number")
	}
	if !strings.Contains(s, "praise") {
		t.Error("format should contain category")
	}
}

func TestFormatResultsEmpty(t *testing.T) {
	s := FormatResults(nil)
	if s != "" {
		t.Errorf("empty results should return empty string, got %s", s)
	}
}

